#!/bin/bash

# Скрипт для быстрого просмотра последних изменений

echo "📋 Последние 5 коммитов:"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
git log --oneline -5
echo ""

echo "📊 Статистика изменений в последних 3 коммитах:"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
git diff HEAD~3 HEAD --stat
echo ""

echo "📝 Изменения в последнем коммите:"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
git show HEAD --stat
echo ""

echo "💡 Для просмотра полных изменений используй:"
echo "   git show HEAD              - последний коммит"
echo "   git show HEAD~1            - предпоследний коммит"
echo "   git diff HEAD~3 HEAD       - изменения за 3 коммита"
echo "   git log -p -3              - полный diff последних 3 коммитов"
